#include<iostream>
using namespace std;
int main()
{
    int iNo = 5;
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo; iCnt ++)
    {
        cout<<"Jay Ganesh...\n";
    }
    return 0;
}